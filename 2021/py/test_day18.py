def test_explode():
    pair = [[[[[9,8],1],2],3],4]
    explode(pair)
    assert pair == [[[[0,9],2],3],4]

    pair = [7,[6,[5,[4,[3,2]]]]]
    explode(pair)
    assert pair == [7,[6,[5,[7,0]]]]

    pair = [[6,[5,[4,[3,2]]]],1]
    explode(pair)
    assert pair == [[6,[5,[7,0]]],3]

    pair = [[3,[2,[1,[7,3]]]],[6,[5,[4,[3,2]]]]]
    explode(pair)
    assert pair == [[3,[2,[8,0]]],[9,[5,[4,[3,2]]]]]

    pair = [[3,[2,[8,0]]],[9,[5,[4,[3,2]]]]]
    explode(pair)
    assert pair == [[3,[2,[8,0]]],[9,[5,[7,0]]]]

def test_split():
    pair = [1, 11]
